// Loops

// while loop
let count = 1;
while (count <= 5) {
  console.log('Count:', count);
  count++;
}

//do while loop
let i = 1;
do {
  console.log('Do while count:', i);
  i++;
} while (i <= 5);

// For Loop
for (let i = 1; i <= 5; i++) {
  console.log('Count:', i);
}

